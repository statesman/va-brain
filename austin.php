	<p>The next year, officials shut down research on the scanner because its images were so poor.</p>
	<p>Today, the Center of Excellence’s powerful scanner sits unused, and the brain imaging research program has yet to produce a single published study.</p>
	<p>Van Boven, meanwhile, is conducting an MRI-based study of TBI at Fort Hood aimed at helping wounded soldiers regain brain activity.</p>
