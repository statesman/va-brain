	<h1>Troubled beginnings</h1>
	<h3>Waco MRI research project had roots in failed UT program</h3>
	<h4 class="byline">Story by <a href="mailto:jschwartz@statesman.com">Jeremy Schwartz</a>, American-Statesman Staff</h4>
 	<h4 style="text-transform: none;">Sept. 7, 2014</h4>
	<p><span class="dropcap">S</span>ix years ago, the Waco Department of Veterans Affairs brain imaging program emerged from the ashes of another troubled Central Texas VA brain research endeavor.</p>
	<p>In 2004, the VA approved $6.3 million to establish a Texas brain imaging center that would study traumatic brain injury using an MRI scanner. Two years later, it opened the Brain Imaging and Recovery Laboratory at the University of Texas in Austin, with hopes of studying and treating thousands of Texas veterans.</p>
	<p>But the lab was plagued by difficulties from the beginning. A director wasn’t hired until 2007, and, by the time he arrived, director Dr. Robert Van Boven claimed that $2.1 million had already been squandered, much of it on diabetes research that had nothing to do with traumatic brain injury. VA administrators refused to investigate his claims, he added.</p>
	<p>A VA inspector general’s report partially substantiated his allegations that the new lab had mismanaged its money prior to his arrival, and Van Boven filed a lawsuit alleging whistleblower retaliation and wrongful termination after the VA fired him in 2009. The suit was settled in 2010 for an undisclosed sum.</p>

	<?php include "tease-main-story.html";?>

	<p>Research at the embattled lab, which had rented an MRI from UT, was shut down in 2008 without testing a single veteran with TBI, sparking congressional outrage.</p>
	<p>“The VA squandered its funds and missed an opportunity to contribute to the body of knowledge on these complex battlefield wounds,” Sen. Richard Burr, R-N.C., ranking member of the Senate Committee on Veterans’ Affairs, wrote to VA administrators in 2010. “This letter seeks clear answers about the shoddy administration of the Brain Injury and Recovery Laboratory in yet another effort to reinforce the need for proper oversight and responsible spending at VA.”</p>
	<p>The agency’s solution was to move the UT lab’s remaining assets to the new Center of Excellence in Waco. The VA Office of Research and Development predicted great success for an imaging program in Waco, which in 2007 purchased a $3.6 million mobile MRI scanner to study troops and veterans.</p>
	<p>The new program, officials said in 2009, would benefit from a “subject pool that, due to proximity to Fort Hood, may be unparalleled nationally in terms of access to and numbers of those returning from (Iraq and Afghanistan). The neuroimaging capabilities for research emanating from the COE, Waco, are cutting-edge.”</p>
	<p>In 2010, officials told Congress the center’s imaging program was well on the road to productivity and claimed neuroimaging investigators were working on five grants, all funded for the next four years. <span id="dct01" class="dcnote">“(The Center of Excellence) is making progress towards improved understanding of the functional aspects of TBI and” post-traumatic stress disorder, VA officials assured the legislators.</span></p>
