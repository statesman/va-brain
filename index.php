	<h3>Shut it down</h3>
	<p><span class="dropcap">T</span>he scanner’s first project was a study seeking to determine the genetic and physical root causes of PTSD, conducted by Keith Young, who served as acting director of the neuroimaging program, even though, colleagues said, he had little imaging experience. By early 2011, Young had scanned more than 200 veterans with the machine, according to VA documents.</p>

		<?php include "photo-gulliversteelelittle.html";?>

	<p>But the image quality was so concerning that in March 2011 the VA brought in an outside expert, Deborah Little, the director of MRI research at the University of Illinois-Chicago, to investigate, according to documents.</p>
	<p>Little’s verdict was devastating: The scanner wasn’t capable of conducting the research it had been purchased to do and needed massive repair. The center’s then-director, Suzy Gulliver, immediately shut down the PTSD study and suspended research on the scanner. (Greg Harrington, an MRI physicist and former researcher at the center, disputed Little’s assessment, saying that when he left the VA in May 2011, the scanner was “fully capable” of performing most research.)</p>
	<p>In July 2011, the scanner suffered a massive failure called a quench, in which the liquid helium used to cool the powerful magnet was released as a gas after the scanner’s cooling system failed. Unplanned quenches can permanently damage magnets or lead to repeated quenching, and in this case it required several weeks of repair.</p>
	<p>The next month, Little joined the VA to permanently oversee the machine and imaging program. A press release announcing her hiring made no mention of the dire situation facing the center’s signature piece of equipment.</p>
	<p>For much of the next year, Philips repaired and redesigned the scanner, at no cost to the VA. The machine was finally returned to the VA at the end of 2012.</p>

