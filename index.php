	<p>“For much of the last three to four decades, (research) was predicated on the idea that brains can’t heal,” said Jim Misko, a former board member of the Brain Injury Association of America. “But in the last 10 years, that’s been completely replaced by the data driven, informed view that a lot of damage from concussion or TBI isn’t black and white. It’s not a question of healthy vs. dead tissue. Most of the damage is in between, in tissue that can be repaired. Now the lid is off with people (wanting) to do research.”</p>
	<p>He added, “If you have access to the best imaging, you’re getting to see damage where we could never see it before.”</p>
	<p>But few brain injury studies in recent years have scanned soldiers before and after deployments. Such a comparison would allow researchers to observe changes in individual soldiers’ brains as the result of exposure to war.</p>
	<p>“It’s a perfect study design,” said Martha Shenton, director of the Psychiatry Neuroimaging Laboratory at Harvard University and scientist at the VA Boston Healthcare System. “The gold standard is the pre-deployed brain.”</p>
	<p>Waco’s research plans represented more than just a better understanding of PTSD and TBI; they were part of an effort to resuscitate the sprawling, 75-year-old Waco VA complex, which officials had targeted for closure in 2003. Thanks to local veterans advocates and lawmakers, a federal panel recommended in 2005 that the red brick buildings in Waco not only be kept open, but expanded. At the 2008 ceremony, Edwards declared: “This is like the phoenix rising from the ashes.”</p>
	<p>The centerpiece of the rebirth was the Philips Achieva Quasar mobile MRI unit. The VA hailed the unit as “the world’s most powerful research (mobile) magnetic resonance imaging (MRI) machine.” It featured a magnet twice as strong as nearly every other mobile unit at the time, which should have given researchers greater speed and image detail.</p>
	<p>“They were swinging for the fences,” said Russell. “There’s only half a dozen of those ever built. So this was like pushing the limits of science, right?”</p>

