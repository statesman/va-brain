	<h4 class="byline">Story by <a href="mailto:jschwartz@statesman.com">Jeremy Schwartz</a>, American-Statesman Staff</h4>
 	<h4 style="text-transform: none;">Sept. 7, 2014</h4>
	<p><span class="dropcap">O</span>n the morning of July 1, 2008, Department of Veterans Affairs officials gathered to unveil a state-of-the-art brain scanner they predicted would help revolutionize the understanding of traumatic brain injury and post-traumatic stress disorder in combat veterans.</p>
	<p>The timing, and location, seemed perfect. One of the first studies would scan nearby Fort Hood soldiers before and after they deployed to war in Iraq or Afghanistan — a unique opportunity to study physical changes in soldiers’ brains due to combat.</p>
	<p>Six years later, the $3.6 million machine sits unused in an out-of-the way corner at the Olin E. Teague Veterans Medical Center in Temple.</p>

	<?php include "tease-comments.html";?>

	<p>Not a single study based on the machine’s scans has been published.</p>
	<p>Not a single veteran has received improved treatment because of advances ushered in by the scanner.</p>
	<p>The machine has sat dormant for the past three years, plagued by a series of delays caused by mismanagement, mechanical failures and bureaucratic roadblocks. Officials at the Waco Center of Excellence for Research on Returning War Veterans, which oversees the program, aborted the scanner’s first and only brain study in 2011 when they declared its image quality too poor to use.</p>
	<p>In a grim internal assessment, the center’s associate research director, Dena Davidson, wrote in March 2013: <span id="dct01" class="dcnote">“I think there should be serious consideration of returning the MRI from where it came because we do not have the expertise to use it or care for it.” [Click to see source document.]</span></p>
	<div id="dc01">
		<div id="DC-note-166326" class="DC-note-container"></div><script src="//s3.amazonaws.com/s3.documentcloud.org/notes/loader.js"></script><script>  dc.embed.loadNote('//www.documentcloud.org/documents/1202679-researchagreementsaga/annotations/166326.js');</script>
		<br>
	</div>
	<p>The scanner idles 24 hours a day because it’s more expensive to turn an MRI machine off and on than to keep it running. A full-time technician diligently performs daily maintenance checks on the unit.</p>
	<p>By early 2014, VA staffers were seeking alternative purposes for what was once envisioned as support space for the multimillion-dollar scanner.</p>
	<p>One idea: housing for lab rats.</p>
	<p>“Can I store my 14 rodent housing racks (2’x6’x7’) in there?” one VA employee asked colleagues in a January email. “This is not a joke.”</p>

	<?php include "video-edwards.html";?>

	<p>It was an inglorious decline for a machine once hailed by VA leaders as the most powerful mobile MRI on the planet. The scanner, housed in a semi-truck trailer, was supposed to travel between Fort Hood, the nation’s busiest deployment hub for war-bound soldiers, and the VA hospitals in Temple and Waco.</p>
	<p>Internal VA emails, reports and documents detail a program that was bungled almost from the start. Yet the story of how one of the agency’s most powerful diagnostic tools devolved into a ghost machine also stands as a stark symbol of the VA’s shortcomings in responding to the specialized needs of soldiers returning from the longest-running conflicts in the country’s history.</p>
	<p>“Everyone involved in this effort felt this was a unique opportunity to help our troops, not just at Fort Hood, but throughout the country,” said former U.S. Rep. Chet Edwards, D-Waco, who had worked to bring the center and mobile MRI to Waco. “I had hopes that this project would work at a time when troops were still deploying to Iraq and Afghanistan. I don’t understand why that didn’t happen. There may be a good reason. I simply do not know.”</p>

