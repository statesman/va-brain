	<h3>Trouble from the beginning</h3>
	<p><span class="dropcap">I</span>n 2009, the VA signed an agreement with Fort Hood to study soldiers before and after they deployed to war. An early contract called for the scanner to make up to 100 trips per year to scan veterans and soldiers.</p>
	<p>Yet moving the scanner, giving researchers access to more subjects, proved problematic.</p>
	<p>Technical problems surfaced almost immediately. Images suffered from “artifacts” or lines and spots caused by vibrations and other disruptions in the scanner’s magnetic field.</p>
	<p>In a statement to the Statesman, Philips said the scanner suffered from “out-of-specification environmental conditions at the site that affected system performance,” but wouldn’t elaborate. The OshKosh Corp., which manufactured the trailer, didn’t respond to a request for comment.</p>
	<p>“You have to recalibrate it, anything in the environment has be factored, it has to be shimmed,” Russell said, adding that it costs thousands of dollars each time it is moved. “So you don’t move it that often,” he said.</p>

	<?php include "tease-va-brain-history.html";?>

	<p>At the same time, the Center of Excellence was struggling to find a neuroimaging director and recruiting imaging researchers.</p>
	<p>“They ventured into MRI research projects without having anyone on board who knew what they were doing,” said another former center employee, who asked for anonymity because he feared retaliation for speaking out. “They threw money at it, but didn’t have anyone in place to get it going. Waco was a tough draw, but a congressman pushed for it in Waco, and so that forced the VA to try and bring people there. It’s hard to build a program from scratch.”</p>
	<p>The employee added that the imaging problems came after the VA failed to conduct proper acceptance testing, which is routinely done to determine if a new research scanner is working as the manufacturer claims.</p>
	<p>“It’s all good intentions,” Russell said. But “by the time we actually had somebody on board who could do those studies, a period of time had already passed. But that’s the difference between being a startup organization and purchasing something for an existing institution.”</p>
