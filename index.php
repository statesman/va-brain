	<h3>‘Adversarial at best’</h3>
	<p><span class="dropcap">Y</span>et there were more glitches. Little told her superiors she needed a research agreement with Philips, which she said would give her the necessary software codes to properly calibrate the machine for advanced research.</p>
	<p>For over a year, Little and her staff haggled over the agreement, with no success. Her biggest obstacle wasn’t Philips, but her colleagues: According to internal VA emails, the VA’s own contracting officials in Illinois refused to release the documents that Center of Excellence staffers needed to execute the agreement.</p>
	<p><span id="dct02" class="dcnote">“We cannot conduct research on our MRI until we have a research agreement in place,” Little wrote to her contracting colleagues in a Jan. 7, 2013, email. “This is a critical issue.”</span></p>
	<div id="dc02">
