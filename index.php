	<h3>VA research arm escapes public scrutiny</h3>
	<p><span class="dropcap">I</span>n recent months, a burgeoning national scandal over how long veterans have to wait for medical care has spurred congressional and criminal investigations and toppled the former VA secretary. Lawmakers have also probed the agency’s disproportionate use of painkillers, high rate of veteran suicide and massive backlogs of disability claims.</p>
	
	<?php include "graphic-deploytbiptsd.html";?>

	<p>The scrutiny, however, has largely ignored the VA’s $2 billion-a-year research arm. A 10-month American-Statesman investigation suggests that the VA’s research arm, charged with developing the military’s treatments of the future, also merits close examination.</p>
	<p>The newspaper reviewed <a href="document-list.php" target="_blank">more than a thousand pages of documents</a> obtained through Freedom of Information Act requests and interviewed six former and current employees of the Center of Excellence, which was to house the mobile MRI and oversee the research.</p>
	<p>The documents and interviews show that leaders took charge of the scanner without a clear plan for success and then were unable to recruit enough researchers, as staffing at the center fell to just 15 employees in January despite initial plans for 75. Later, administrators became mired in red tape; internal VA squabbles paralyzed the imaging program after workers appeared close to restarting research in 2012 — delays that launched a cascade of new problems. The Waco center lost at least seven federally funded grants for what researchers hoped would be groundbreaking brain injury research.</p>
	<p>“They didn’t want people to know how much of a failure it was,” said one former Center of Excellence employee, who requested anonymity because he feared retaliation from the VA. “Unless someone says something, they will think it’s OK to do what they’ve done and continue to do.”</p>
	<p>The program’s failures came just as it was needed most. Since 2008, more than 150,000 U.S. service members have returned from war with diagnoses of PTSD or TBI. Yet research has yielded few significant advancements in treatment of the two maladies, experts say.</p>
	<p>For many vets, “PTSD or TBI are factors in their inability to reintegrate — they come back a changed person, they know it, but they can’t identify why,” said Steve Hernandez, the McLennan County veterans service officer. “To know we had seven years where we could have helped find breakthroughs in understanding, that’s disheartening.”</p>

	<?php include "tease-va-brain-austin.html";?>

	<p>	Waco resident Timothy Priddy, who suffered a traumatic brain injury when he deployed to Iraq with Fort Hood’s 1st Cavalry Division in 2004, wonders if the scanner could have led to advances that might have helped him get better treatment.</p>
	<p>“The way they were talking, (the scanner) could see more thoroughly into the brain and better detect brain injuries and everything,” Priddy, 35, said. “(The center’s failures) were a slap to all veterans that go out there. There’s no telling how many Vietnam veterans have TBIs.”</p>
	<p>The tumult at the Waco Center of Excellence also caps nearly a decade of VA futility in Central Texas when it comes to researching brain injuries. Five years ago, the VA shut down research at a similar program at the University of Texas in Austin — the Brain Injury and Recovery Laboratory, which didn’t scan a single veteran before its assets were transferred to Waco. Between them, the two imaging programs cost taxpayers more than $12 million and squandered almost a decade of opportunity.</p>
	<p>Today, VA officials say they are trying to revive the Waco program and find a researcher to take charge of the troubled scanner. They have hired a new center director, added employees and hope to eventually grow to 50 staffers.</p>
	<p>“You know, I can do what I can do,” said the center’s new director Michael Russell. “I’ll get it going, and I think there’s going to be a continuing (operational) tempo of deployments for at least some time. It will be smaller numbers, but there will still be deployers.”</p>
	<p>The VA did not respond to a request for comment from administrators who oversaw the program during the previous six years.</p>

