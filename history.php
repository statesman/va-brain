	<h1>Gaps in research</h1>
	<h3>Waco center was to play a big role in government brain studies</h3>
	<h4 class="byline">Story by <a href="mailto:jschwartz@statesman.com">Jeremy Schwartz</a>, American-Statesman Staff</h4>
 	<h4 style="text-transform: none;">Sept. 7, 2014</h4>
	<p><span class="dropcap">O</span>ver the past decade, the federal government has galvanized research into post-traumatic stress disorder and traumatic brain injury. The injuries are considered the signature wounds of the Afghanistan and Iraq conflicts, which have seen a greater percentage of blast injuries than in Vietnam and World War II, when gunshot injuries were more prevalent.</p>

	<?php include "graphic-injuries.html";?>

	<p>The Defense Department calculates 500,000 service members, both deployed and nondeployed, have been diagnosed with the injuries during the two wars, often caused by buried roadside bombs. That has spurred $700 million in military spending on more than 500 brain trauma research projects since 2007 alone, according to Army officials. The Department of Veterans Affairs has spent an additional $160 million on brain injury research since 2008, about 4 percent of its research and prosthetics budget.</p>
	<p>Increasingly powerful MRI machines that capture brain images in ever-greater detail, such as the ill-fated mobile scanner purchased by the Waco VA’s Center of Excellence in 2007, have played a crucial role. Much of the research is aimed at uncovering physical evidence of the unseen injuries.</p>
	<p>VA researchers in North Carolina have used MRIs to find physical indications of PTSD in subjects who were scanned while looking at traumatic photos. A Harvard University group has used the scanners to detect damage to the connections between brain neurons that could offer insight into difficult to diagnose mild traumatic brain injuries.</p>
	<p>Few, if any, other studies have conducted MRI scans on the same troops before and after combat deployments, as officials in Waco had hoped to do.</p>

	<?php include "tease-main-story.html";?>

	<p>Such a pre- and post-deployment protocol is considered the gold standard and has proven effective in nonimaging studies. In San Diego, VA researchers recently assessed 1,650 Camp Pendleton Marines before and after combat tours, finding that physical brain trauma is an indicator of PTSD risk. Researchers said the findings could translate to faster and more effective treatment.</p>
	<p>Despite the isolated successes, leaders and veterans advocates have complained of the slow pace of progress. Retired Gen. Peter Chiarelli, who now heads One Mind for Research, a nonprofit dedicated to brain injury research, said brain science is decades behind researchers’ understanding of maladies affecting other parts of the body. Brain injury diagnosis remains a “crapshoot,” he told the American-Statesman in April, adding that the research gap has hobbled efforts to produce effective treatment.</p>
	<p>One problem was that the various projects lacked coordination. Brain injury research “being conducted is small-scale, siloed, and data and results are not shared,” One Mind concluded. In June, the Institute of Medicine, a nonprofit that advises government officials on health issues, agreed that the VA and Defense Department lack a consistent, “evidence -based” system to treat PTSD.</p>
	<p>A recent American Legion survey of injured veterans found that 59 percent of those with traumatic brain injuries or PTSD reported their medical treatment either brought no improvement or made them feel worse.</p>

	<?php include "photo-returnfromiraq.html";?>

	<p>In response to the sluggish pace of research, in August 2013 the departments of Defense, Veterans Affairs, Health and Human Services and Education laid out plans for a coordinated push, similar to national and international efforts to reduce HIV deaths, to determine “the neural basis of PTSD and the functional deficits associated with TBI.”</p>
	<p>At the heart of the effort is a plan to use sophisticated brain imaging tools to uncover the damage done by blast injuries and help discover new treatments. It’s unclear, however, if the Waco MRI program will be revived in time to take part.</p>

